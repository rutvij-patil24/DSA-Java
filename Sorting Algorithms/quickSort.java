public class quickSort {
    
    public static void main(String[] args) {
        
        int nums[] = {34, 7, 23, 34, 5, 62, 12, 15, 78, 1};


        System.out.println("Before sorting:");
        for (int num : nums) {
            System.out.print(num + " ");
        }

        



        System.out.println();
        System.out.println("After sorting:");
        for (int num : nums) {
            System.out.print(num + " ");
        }

    }
}
